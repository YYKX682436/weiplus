import os
path = r"app\src\main\java\com\muchen\weiplus\features\GameCheatFeature.kt"
os.makedirs(os.path.dirname(path), exist_ok=True)

L = []
def a(s): L.append(s)

a("package com.muchen.weiplus.features")
a("")
a("import android.app.AlertDialog")
a("import android.os.Handler")
a("import android.os.Looper")
a("import android.util.Log")
a("import android.view.View")
a("import android.view.ViewGroup")
a("import io.github.libxposed.api.XposedModule")
a("")
a("class GameCheatFeature : BaseFeature() {")
a("")
a("    companion object { private const val TAG = \"GameCheat\" }")
a("    override val key = \"game_cheat\"")
a("    override val name = \"Game Cheat\"")
a("")
a("    private lateinit var module: XposedModule")
a("    private var classLoader: ClassLoader? = null")
a("    private val mainHandler = Handler(Looper.getMainLooper())")
a("")
a("    override fun onEnable(module: XposedModule, classLoader: ClassLoader) {")
a("        this.module = module")
a("        this.classLoader = classLoader")
a("        hookViewClick()")
a("        hookAllGameClasses(classLoader)")
a("        module.log(Log.INFO, TAG, \"OK\")")
a("    }")
a("")
a("    // ========= View.click hook - diagnostic mode =========")
a("")
a("    private fun hookViewClick() {")
a("        try {")
a("            val m = View::class.java.getDeclaredMethod(\"performClick\")")
a("            module.hook(m).intercept { chain ->")
a("                val v = chain.thisObject as? View ?: return@intercept chain.proceed()")
a("                val cn = v.javaClass.name")
a("")
a("                // Build parent chain")
a("                var parentChain = \"\"")
a("                var p: Any? = v.parent")
a("                var d = 0")
a("                while (p != null && d < 6) {")
a("                    parentChain += \" -> \" + p.javaClass.name")
a("                    p = (p as? ViewGroup)?.parent")
a("                    d++")
a("                }")
a("")
a("                // Try getting text if TextView")
a("                val txt = try { (v as? android.widget.TextView)?.text?.toString()?.take(30) ?: \"\" } catch (_: Throwable) { \"\" }")
a("                val tag = try { v.tag?.toString()?.take(60) ?: \"\" } catch (_: Throwable) { \"\" }")
a("")
a("                module.log(Log.INFO, TAG, \"CLICK $cn txt=$txt tag=$tag parents=$parentChain\")")
a("                chain.proceed()")
a("            }")
a("            module.log(Log.INFO, TAG, \"View.click hooked\")")
a("        } catch (e: Throwable) { module.log(Log.ERROR, TAG, \"View.click: ${e.message}\") }")
a("    }")
a("")
a("    // ========= Diagnostic hooks =========")
a("")
a("    private fun hookAllGameClasses(cl: ClassLoader) {")
a("        val targets = listOf(")
a("            \"com.tencent.mm.storage.emotion.EmojiInfo\",")
a("            \"com.tencent.mm.plugin.emoji.model.EmojiInfo\",")
a("            \"com.tencent.mm.plugin.emoji.b.d\",")
a("        )")
a("        for (cn in targets) {")
a("            try {")
a("                val cls = cl.loadClass(cn)")
a("                for (m in cls.declaredMethods) {")
a("                    if (m.parameterTypes.size in 0..6) {")
a("                        module.hook(m).intercept { chain ->")
a("                            if (!FeatureConfig.gameCheat) return@intercept chain.proceed()")
a("                            val sb = StringBuilder()")
a("                            for (i in chain.args.indices) {")
a("                                val a = chain.args[i]")
a("                                val s = if (a is String) a.take(80) else a?.toString()?.take(40) ?: \"null\"")
a("                                if (s != \"null\" && s.isNotEmpty()) sb.append(\"a$i=$s | \")")
a("                            }")
a("                            val result = chain.proceed()")
a("                            if (result is String && result.length > 5) sb.append(\"ret=${result.take(120)}\")")
a("                            if (sb.isNotEmpty()) module.log(Log.INFO, TAG, \"$cn.${m.name}: $sb\")")
a("                            result")
a("                        }")
a("                    }")
a("                }")
a("                module.log(Log.INFO, TAG, \"$cn hooked\")")
a("            } catch (_: Throwable) {}")
a("        }")
a("    }")
a("}")
a("")
with open(path, "w", encoding="utf-8") as f:
    f.write("\n".join(L))
print("OK")
